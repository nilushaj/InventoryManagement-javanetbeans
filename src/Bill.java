    private void btngoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btngoActionPerformed

        try {

            int iid = Integer.parseInt(txtitemid.getText());
            String setitemname = "Select itemname,packsize from itemfl where itemcd='" + iid + "'";
            ResultSet rs3 = stmt.executeQuery(setitemname);
            rs3.next();
            String itemname = rs3.getString("itemname");
            float itemqtyt = rs3.getFloat("packsize");
            String updatepack = "Update itemfl set packsize=packsize-'" + txtqnt.getText() + "' where itemcd='" + iid + "'";
            stmt.executeUpdate(updatepack);
            int bid = Integer.parseInt(txtbillid.getText());
            float iqt = Float.parseFloat(txtqnt.getText());
            float nprice = Float.parseFloat(txtprice.getText());
            float dscnt = Float.parseFloat(txtdscnt.getText());
            float price = Float.parseFloat(txttot.getText());
            float citem = 0;
            float cprice = 0;
            int check = 0;
            String chid = "Select itemcd from invfl where invno='" + bid + "' ";
            ResultSet rsid = stmt.executeQuery(chid);
            while (rsid.next()) {
                if (rsid.getInt("itemcd") == iid) {
                    check++;
                }
            }
            if (check != 0) {
                String chsql = "Select Item_Qnt,Price from invfl where itemcd='" + iid + "'AND invno='" + bid + "'  ";
                ResultSet rsch = stmt.executeQuery(chsql);
                rsch.next();
                citem = rsch.getFloat("Item_Qnt");
                cprice = rsch.getFloat("Price");
                float nwqnt = iqt + citem;
                float nwprice = cprice + tot;
                stmt.executeUpdate("Update invfl SET Item_Qnt='" + nwqnt + "',  Price='" + nwprice + "'  where itemcd='" + iid + "' AND invno='" + bid + "' ");
            } else {
                String inv = "Insert into invfl values('" + bid + "','" + d + "','" + iid + "','" + itemname + "','" + iqt + "','" + nprice + "','" + dscnt + "','" + price + "','" + payterm + "','" + txtcardno.getText() + "','" + txtstatus.getText() + "','" + logid + "')";
                stmt.executeUpdate(inv);
            }
            String tblsql = "Select Item_Name,Item_Qnt,Net_Price,Discount,Price from invfl where invno= '" + bid + "'";
            ResultSet rs1 = stmt.executeQuery(tblsql);
            tblinv.setModel(DbUtils.resultSetToTableModel(rs1));
            String settotal = "Select sum(price) as tot from invfl where invno='" + bid + "'";
            ResultSet rstot = stmt.executeQuery(settotal);

            rstot.next();
            float total = rstot.getFloat("tot");
            txttotal.setText(df.format(total));
            ++lst;
            tot = 0;
            txtitemid.setText("");
            txtqnt.setText("");
            txtprice.setText("");
            txtdscnt.setText("");
            txttot.setText("");
            if (lst != 0) {
                lstpayterm.setEnabled(false);
                txtcardno.setEditable(false);
                txtstatus.setEditable(false);
            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);

        }

    }//GEN-LAST:event_btngoActionPerformed

