    private void txttotMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_txttotMouseClicked

        float dscnt = Float.parseFloat(txtdscnt.getText());
        float price = Integer.parseInt(txtprice.getText());
        float qnt = Integer.parseInt(txtqnt.getText());
        float calds = (dscnt / 100) * price;
        tot = (price - calds) * qnt;

        String totl = Float.toString(tot);
        txttot.setText(totl);

    }//GEN-LAST:event_txttotMouseClicked

