    private void formWindowGainedFocus(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowGainedFocus
        txtlogid.setText(uid);
        String chcksql = "Select User_ID from userfl ";
        String chqmin = "Select itemcd,packsize,minqty from itemfl";
        try {
            ResultSet rs = stmt.executeQuery(chcksql);
            while (rs.next()) {
                String logid = rs.getString("User_ID");
                if (logid.equals(uid)) {
                    btnsup.setEnabled(false);
                    btnitems.setEnabled(false);
                    btnreports.setEnabled(false);
                }

            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex + " System Error!! Contact admin");
        }
    }//GEN-LAST:event_formWindowGainedFocus

