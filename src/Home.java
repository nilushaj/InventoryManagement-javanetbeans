    public final void chqmin() {
        String chqmin = "Select itemcd,packsize,minqty from itemfl";
        ResultSet rs1;
        try {
            rs1 = stmt.executeQuery(chqmin);
            while (rs1.next()) {
                int itemcd = rs1.getInt("itemcd");
                float packsize = rs1.getFloat("packsize");
                float min = rs1.getFloat("minqty");
                if (packsize < min) {
                    JOptionPane.showMessageDialog(null, "Please Re Oder \n" + itemcd);
                }
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }

    }
