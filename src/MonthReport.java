                writer.println("\t\t\tIssued By :" + txtlogid.getText());
                writer.println("\t\t\t\t\tMonthly Report");
                writer.println("\t\t\t\t\tReport For :" + date);
                writer.println();
                writer.println("\t\t\t\tInvdate \t\tItemcd\t\tItem_Name\tItem_Qnt\t\tNet_price\t\tDiscount\t  value");
                writer.println();
