    public final void ConnectionDB() {
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/sampathstores", "root", "123");
            stmt = con.createStatement();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex + "DataAcces Failed!!Contact ADMIN!!");

        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(null, ex + "DataBase Connection Driver not Found!!Contact ADMIN!!");
        }

    }

    public void TableDB() {
        try {

            String tblsql = "Select invdate,itemcd,Item_Name,Item_Qnt ,Net_Price,Discount,Price from invfl where  MONTH(invdate) = '" + month + "' AND YEAR(invdate) ='" + year + "' ";
            ResultSet rs1 = stmt.executeQuery(tblsql);
            boolean val = rs1.next();
            if (val == false) {
                JOptionPane.showMessageDialog(null, "No results founds for this Month and Year");
                  btnprint.setEnabled(false);

            } else {
                tblReport.setModel(DbUtils.resultSetToTableModel(rs1));
            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);

        }

    }

    public void Total() {
        try {

            String count = "Select SUM(Price) AS total  from invfl where   MONTH(invdate) = '" + month + "' AND YEAR(invdate) ='" + year + "'  ";
            ResultSet rs1 = stmt.executeQuery(count);
            rs1.next();
            total1 = rs1.getFloat("total");
            txttotal.setText(Float.toString(total1));

        } catch (SQLException ex) {

            JOptionPane.showMessageDialog(null, ex);
            this.setVisible(false);

        }

    }

