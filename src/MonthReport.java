                    writer.println("\t\t\t" + rsf.getString("invdate") + "\t" + rsf.getInt("itemcd") + "\t\t" + rsf.getString("Item_Name") + "\t\t" + rsf.getFloat("Item_Qnt") + "\t\t" + rsf.getFloat("Net_Price") + "\t\t" + rsf.getFloat("Discount") + "\t" + rsf.getFloat("Price"));
